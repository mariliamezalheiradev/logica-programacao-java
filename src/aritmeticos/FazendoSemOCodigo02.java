package aritmeticos;

public class FazendoSemOCodigo02 {
    public static void main(String[] args) {
        int estoque = 0;
        estoque += 10; // produto 1
        estoque -= 3; // produto 2
        estoque += 5; // produto 3
        estoque -= 2; // produto 4
        estoque += 4; // produto 5
        System.out.println("Total de produtos no estoque: " + estoque);
    }
}
