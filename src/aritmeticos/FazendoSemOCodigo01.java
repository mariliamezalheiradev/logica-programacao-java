package aritmeticos;

public class FazendoSemOCodigo01 {
    public static void main(String[] args) {
        // Fazendo sem o código.
        int passageiros = 0;
        passageiros += 2; // embarque 1
        passageiros++; // embarque 2
        passageiros--; // embarque 3
        System.out.println("Total de passageiros: " + passageiros);
    }
}
