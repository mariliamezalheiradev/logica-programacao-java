package Bloco03IfElse;

public class Bloco03IfElse11 {
    public static void main(String[] args) {
        // Crie um programa que receba um número e verifique se ele é POSITIVO ou NEGATIVO.
        int numero = 11;

        if (numero >= 0){
            System.out.println("É positivo");
        }else if (numero < 0){
            System.out.println("É negativo");
        }else {
            System.out.println("É zero");
        }
    }
}
