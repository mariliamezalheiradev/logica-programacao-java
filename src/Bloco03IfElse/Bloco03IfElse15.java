package Bloco03IfElse;

public class Bloco03IfElse15 {
    public static void main(String[] args) {
        double nota = 4;

        if (nota >= 7) {
            System.out.println("Aprovado");
        } else if (nota >= 5) {
            System.out.println("Recuperação");
        }else {
            System.out.println("Reprovado");
        }
    }
}
