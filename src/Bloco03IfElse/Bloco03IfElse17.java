package Bloco03IfElse;

public class Bloco03IfElse17 {
    public static void main(String[] args) {
        char letra = 'a';

        if (letra == 'a' || letra == 'e' || letra == 'i' || letra == 'o' || letra == 'u'){
            System.out.println("É vogal");
        }else {
            System.out.println("É consoante");
        }
    }
}
