package comparacao;

public class FazendoComOCodigo {
    public static void main(String[] args) {
        int temperatura = 32;

        if (temperatura >= 30) {
            System.out.println("Está quente aqui!");
        } else if (temperatura >= 20) {
            System.out.println("A temperatura está agradável aqui!");
        } else {
            System.out.println("Está frio aqui!");
        }
    }
}
