package comparacao;

public class FazendoSemOCodigo07 {
    public static void main(String[] args) {
        int temperatura = 30;

        if (temperatura >= 35) {
            System.out.println("Está muito quente!");
        } else if (temperatura >= 25) {
            System.out.println("O clima está agradável.");
        } else {
            System.out.println("Está frio.");
        }
    }
}
