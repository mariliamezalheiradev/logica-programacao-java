package Bloco02OperadoresAritmeticos;

public class Bloco02Aritmeticos06 {
    public static void main(String[] args) {
        // Crie um programa que receba dois números e exiba a soma, subtração, multiplicação e divisão deles.
        int numero01 = 301;
        int numero02 = 302;
        int resultado = numero01 + numero02;
        int resultado2 = numero01 - numero02;
        int resultado3 = numero01 * numero02;
        int resultado4 = numero01 / numero02;
        System.out.println(resultado);
        System.out.println(resultado2);
        System.out.println(resultado3);
        System.out.println(resultado4);
    }
}
