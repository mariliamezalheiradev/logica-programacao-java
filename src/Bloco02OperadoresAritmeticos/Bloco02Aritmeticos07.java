package Bloco02OperadoresAritmeticos;

public class Bloco02Aritmeticos07 {
    public static void main(String[] args) {
        // Declare duas variáveis (a=10, b=3) e imprima o resultado de a + b, a - b, a * b, a / b e a % b.

        int a = 10;
        int b = 3;
        int resultado = a + b;
        int resultado01 = a - b;
        int resultado02 = a * b;
        int resultado03 = a / b;
        int resultado04 = a % b;
        System.out.println(resultado);
        System.out.println(resultado01);
        System.out.println(resultado02);
        System.out.println(resultado03);
        System.out.println(resultado04);
    }
}
